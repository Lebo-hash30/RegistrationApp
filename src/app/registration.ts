export class Registration {
    name: string;
    surname: string;
    id: number;
    email: string;
    contact: number;
    isDone: boolean;
    constructor(name, surname, id, email, contact){
        this.name = name ;
        this.surname = surname ;
        this.id = id;
        this.email = email;
        this.contact = contact;
        this.isDone = false;
    }

    ;
    
}


